#Crie uma tupla com 5 cores e mostre todas elas.

cores = ("Azul", "Verde", "Vermelho", "Preto", "Amarelo")

for cor in cores:
    print(cor)

"""
O que acontece no for cor in cores:
O for pega cada elemento da tupla cores.
A variável cor recebe esse valor um de cada vez.
Então o print(cor) mostra o conteúdo.
"""